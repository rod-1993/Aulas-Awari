// 3. Receber um número inteiro e mostrar a contagem de 1 até ele.

const target = window.prompt('Digite um número inteiro:', '10');

for (let i=1; i<=target; i++) {
    console.log(i);
}

// console.log = Exibe uma mensagem na console do navegador.
// window.prompt = exibe uma janela com uma mensagem + campo para preencher um dado.